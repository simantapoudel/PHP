<?php
 /*
    String manipulation functions

    str_replace() : replaces a word or a character in a string
    Example: 
    hi replaces hello in hello world and outputs hi world. 
    echo str_replace("hello", "hi", "hello world");

    strlen() : returns the length of a string
    Example: 
    strlen("SIMANTA") calculates length of string SIMANTA and outputs 7
    echo strlen("SIMANTA");
 
    str_word_count() : this counts no. of words in a string passed to it
    Example: 
    str_word_count("This is Earth") counts no. of words and returns 3
    echo str_word_count("This is Earth");

    strrev() : this reverses the string passed to it
    Example: 
    strrev("EARTH") reverses EARTH and returns HTRAE
    echo strrev("EARTH");

    strpos() : this finds the position of a character in the given string
    Example: 
    strpos("EARTH", "R") finds position of R in EARTH and returns 2
    echo strpos("EARTH", "R");
 */
?>
